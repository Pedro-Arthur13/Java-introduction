package net.arthur;

public class AssignmentOperators {
    public static void main(String[] args) {
        // Assignment Operators

        int x = 100;
        int y = 20;

        x += y; // Plus equals
        System.out.println(x);
        x -= y; // Minus equals
        System.out.println(x);
        x *= y; // Times equals
        System.out.println(x);
        x /= y; // Divide equals
        System.out.println(x);
        x %= y; // Reminds equals
        System.out.println(x);

        //Incrementing
        // Add just 1 at the time
        x++;
        System.out.println(x);
        //Decrementing
        y--;
        System.out.println(y);

    }
}
